"""Wide chat-bots, relying on a single prompt and LLM to handle various user intents like in the zero shot agent example, can be difficult to manage.

But when tasks becomes more complex we may need specialized agents and workflows in owr Graph.
Each workflow can focus on its domain, allowing for isolated improvements without degrading the overall assistant.

In complex graph we may want to trak the current state of which sub-graph is in control at any given moment.
We can manage this by adding more fields in the Graph state, such as the current sub-graph name or the current node name."""

